void	ft_short_parm(char *parm, char *parm_s, int size)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (parm[i] != '\0' && j < size - 2)
	{
		parm_s[j] = parm[i];
		i = i + 2;
		j++;
	}
	parm_s[j] = parm[30];
	parm_s[j + 1] = '\0';
}
